def _get_custom_slug(cat_name):
    """–ü–æ–ª—É—á–∏—Ç—å –∫–∞—Å—Ç–æ–º–Ω—ã–π slug –¥–ª—è –∫–∞—Ç–µ–≥–æ—Ä–∏–∏"""
    custom_slugs = {
        '–ö–æ—Å–º–æ—Å': 'space',
        '–í–æ–π–Ω–∞': 'war',
        '–ü–æ–ª–∏—Ç–∏–∫–∞': 'politics'
    }
    
    if cat_name in custom_slugs:
        return custom_slugs[cat_name]
    
    # –î–ª—è –æ—Å—Ç–∞–ª—å–Ω—ã—Ö –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—É—é –ª–æ–≥–∏–∫—É
