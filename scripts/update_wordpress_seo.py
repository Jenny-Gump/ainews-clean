def update_categories_seo():
    """–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ SEO –∫–∞—Ç–µ–≥–æ—Ä–∏–π —á–µ—Ä–µ–∑ WordPress API"""
    config = Config()
    auth = HTTPBasicAuth(config.wordpress_username, config.wordpress_app_password)
    
    print('üéØ –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ SEO –∫–∞—Ç–µ–≥–æ—Ä–∏–π —á–µ—Ä–µ–∑ WordPress API')
    print('=' * 60)
    
    # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    categories_response = requests.get(f'{config.wordpress_api_url}/categories', auth=auth, params={'per_page': 100})
    if categories_response.status_code != 200:
        print('‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ –∫–∞—Ç–µ–≥–æ—Ä–∏–π')
        return False
    
    categories = categories_response.json()
    category_map = {cat['name']: cat['id'] for cat in categories}
    
    print(f'üìÇ –ù–∞–π–¥–µ–Ω–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–π: {len(categories)}')
    
    success_count = 0
    
    for cat_name, seo_data in CATEGORIES_SEO.items():
        if cat_name not in category_map:
            print(f'‚ùå {cat_name} –Ω–µ –Ω–∞–π–¥–µ–Ω–∞')
            continue
            
        cat_id = category_map[cat_name]
        print(f'\nüîÑ –û–±–Ω–æ–≤–ª—è–µ–º {cat_name} (ID: {cat_id})...')
        
        try:
            # –û–±–Ω–æ–≤–ª—è–µ–º —á–µ—Ä–µ–∑ WordPress REST API
            update_data = {
                'name': seo_data['name'],
                'description': seo_data['description'],
                'slug': seo_data['slug']
            }
            
            response = requests.post(
                f'{config.wordpress_api_url}/categories/{cat_id}',
                json=update_data,
                auth=auth
            )
            
            if response.status_code == 200:
                result = response.json()
                print(f'‚úÖ {cat_name}: –£—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω–æ')
                print(f'   –ò–º—è: {result.get("name")}')
                print(f'   –û–ø–∏—Å–∞–Ω–∏–µ: {len(result.get("description", ""))} —Å–∏–º–≤–æ–ª–æ–≤')
                print(f'   Slug: {result.get("slug")}')
                
                success_count += 1
                
                # –ü–∞—É–∑–∞ –º–µ–∂–¥—É –∑–∞–ø—Ä–æ—Å–∞–º–∏
                time.sleep(0.3)
            else:
                print(f'‚ùå {cat_name}: –û—à–∏–±–∫–∞ {response.status_code}')
                print(f'   –û—Ç–≤–µ—Ç: {response.text[:200]}')
                
        except Exception as e:
            print(f'‚ùå {cat_name}: –ò—Å–∫–ª—é—á–µ–Ω–∏–µ - {e}')
    
    print(f'\nüìä –†–µ–∑—É–ª—å—Ç–∞—Ç: {success_count}/{len(CATEGORIES_SEO)} –∫–∞—Ç–µ–≥–æ—Ä–∏–π –æ–±–Ω–æ–≤–ª–µ–Ω–æ')
    return success_count > 0


