        logger.info(f"   –ó–∞–¥–µ—Ä–∂–∫–∞ –º–µ–∂–¥—É —Å—Ç–∞—Ç—å—è–º–∏: {delay_between} —Å–µ–∫")
        logger.info("   –î–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ –Ω–∞–∂–º–∏—Ç–µ Ctrl+C")
        
        # –°–æ–∑–¥–∞–µ–º –∑–∞–¥–∞—á–∏ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –≤–æ—Ä–∫–µ—Ä–∞
        tasks = []
        for worker_num in range(num_workers):
            worker_id = f"worker_{worker_num + 1}"
            task = asyncio.create_task(
                run_single_worker(worker_id, max_articles, delay_between),
                name=f"Worker-{worker_id}"
            )
            tasks.append(task)
        
        # Graceful shutdown handler
        stop_event = asyncio.Event()
        
        def signal_handler(sig, frame):
            logger.info("\n‚ö†Ô∏è –ü–æ–ª—É—á–µ–Ω —Å–∏–≥–Ω–∞–ª –æ—Å—Ç–∞–Ω–æ–≤–∫–∏. –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ –≤—Å–µ—Ö –≤–æ—Ä–∫–µ—Ä–æ–≤...")
            stop_event.set()
        
        signal.signal(signal.SIGINT, signal_handler)
        
        # –ó–∞–ø—É—Å–∫–∞–µ–º –≤—Å–µ –≤–æ—Ä–∫–µ—Ä—ã –ø–∞—Ä–∞–ª–ª–µ–ª—å–Ω–æ
        try:
            # –ñ–¥–µ–º –ª–∏–±–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –≤—Å–µ—Ö –∑–∞–¥–∞—á, –ª–∏–±–æ —Å–∏–≥–Ω–∞–ª–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
            done, pending = await asyncio.wait(
                tasks + [asyncio.create_task(stop_event.wait())],
                return_when=asyncio.FIRST_COMPLETED
            )
            
            # –ï—Å–ª–∏ –ø–æ–ª—É—á–µ–Ω —Å–∏–≥–Ω–∞–ª –æ—Å—Ç–∞–Ω–æ–≤–∫–∏, –æ—Ç–º–µ–Ω—è–µ–º –≤—Å–µ –∑–∞–¥–∞—á–∏
            if stop_event.is_set():
                logger.info("–û—Ç–º–µ–Ω—è–µ–º –≤—Å–µ –∞–∫—Ç–∏–≤–Ω—ã–µ –≤–æ—Ä–∫–µ—Ä—ã...")
                for task in pending:
                    task.cancel()
                
                # –ñ–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –æ—Ç–º–µ–Ω–µ–Ω–Ω—ã—Ö –∑–∞–¥–∞—á
                await asyncio.gather(*pending, return_exceptions=True)
            
        except KeyboardInterrupt:
            logger.info("–ü—Ä–µ—Ä—ã–≤–∞–Ω–∏–µ —Ä–∞–±–æ—Ç—ã...")
            for task in tasks:
                task.cancel()
            await asyncio.gather(*tasks, return_exceptions=True)
        
        # –°–æ–±–∏—Ä–∞–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –æ—Ç –≤—Å–µ—Ö –≤–æ—Ä–∫–µ—Ä–æ–≤
        total_processed = 0
        total_success = 0
        total_errors = 0
        total_published = 0
        
        for task in tasks:
            if task.done() and not task.cancelled():
                try:
                    result = task.result()
                    if result:
                        total_processed += result.get('processed_count', 0)
                        total_success += result.get('success_count', 0)
                        total_errors += result.get('error_count', 0)
                        total_published += result.get('wordpress_published', 0)
                except Exception as e:
                    logger.error(f"–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –æ—Ç –≤–æ—Ä–∫–µ—Ä–∞: {e}")
        
        # –í—ã–≤–æ–¥–∏–º —Ñ–∏–Ω–∞–ª—å–Ω—É—é —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
        logger.info("\n" + "="*60)
        logger.info("üìä –§–ò–ù–ê–õ–¨–ù–ê–Ø –°–¢–ê–¢–ò–°–¢–ò–ö–ê –ü–ê–†–ê–õ–õ–ï–õ–¨–ù–´–• –í–û–†–ö–ï–†–û–í:")
